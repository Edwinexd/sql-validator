CREATE UNIQUE INDEX rum_namn ON Seminarraum (name);
CREATE TABLE Student (persNr TEXT NOT NULL, nachteilsausgleich BOOLEAN NOT NULL, CONSTRAINT STUDENT_PK PRIMARY KEY (persNr), CONSTRAINT Student_Person_FK FOREIGN KEY (persNr) REFERENCES Person (persNr) ON DELETE RESTRICT ON UPDATE CASCADE);
CREATE TABLE Dozent (persNr TEXT NOT NULL, bueroNr INTEGER NOT NULL, CONSTRAINT teacher_pk PRIMARY KEY (persNr), CONSTRAINT fk_teacher_person FOREIGN KEY (persNr) REFERENCES Person (persNr) ON DELETE RESTRICT ON UPDATE CASCADE);
CREATE TABLE Modul (kuerzel TEXT NOT NULL, name TEXT NOT NULL, kurstage INTEGER NOT NULL, preis INTEGER NOT NULL, beschreibung TEXT NOT NULL, PRIMARY KEY (kuerzel));
CREATE UNIQUE INDEX kurs_ben ON Modul (name);
CREATE TABLE Kursdurchfuehrung (modulkuerzel TEXT NOT NULL, startdatum DATE NOT NULL, dozentNr TEXT NOT NULL, raumID INTEGER NOT NULL, CONSTRAINT courseinstance_pk PRIMARY KEY (modulkuerzel, startdatum), CONSTRAINT fk_courseinstance_course FOREIGN KEY (modulkuerzel) REFERENCES Modul (kuerzel) ON DELETE RESTRICT ON UPDATE CASCADE, CONSTRAINT fk_courseinstance_teacher FOREIGN KEY (dozentNr) REFERENCES Dozent (persNr) ON DELETE RESTRICT ON UPDATE CASCADE, CONSTRAINT fk_courseinstance_room FOREIGN KEY (raumID) REFERENCES Seminarraum (id) ON DELETE RESTRICT ON UPDATE CASCADE);
CREATE TABLE Teilnehmer (studNr TEXT NOT NULL, modulkuerzel TEXT NOT NULL, startdatum DATE NOT NULL, CONSTRAINT participation_pk PRIMARY KEY (modulkuerzel, startdatum, studNr), CONSTRAINT fk_participation_courseinstance FOREIGN KEY (modulkuerzel, startdatum) REFERENCES Kursdurchfuehrung (modulkuerzel, startdatum) ON DELETE RESTRICT ON UPDATE CASCADE, CONSTRAINT fk_participation_student FOREIGN KEY (studNr) REFERENCES Student (persNr) ON DELETE RESTRICT ON UPDATE CASCADE);
