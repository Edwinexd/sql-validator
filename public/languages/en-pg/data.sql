CREATE TABLE Person (person_id TEXT NOT NULL, name TEXT NOT NULL, address TEXT NOT NULL, postal_code TEXT NOT NULL, city TEXT NOT NULL, phone TEXT NOT NULL, PRIMARY KEY (person_id));
CREATE TABLE Room (id INTEGER NOT NULL, name TEXT NOT NULL, capacity INTEGER NOT NULL, PRIMARY KEY (id));
CREATE UNIQUE INDEX rum_namn ON Room (name);
CREATE TABLE Student (person_id TEXT NOT NULL, has_disability BOOLEAN NOT NULL, CONSTRAINT STUDENT_PK PRIMARY KEY (person_id), CONSTRAINT Student_Person_FK FOREIGN KEY (person_id) REFERENCES Person (person_id) ON DELETE RESTRICT ON UPDATE CASCADE);
CREATE TABLE Teacher (person_id TEXT NOT NULL, office_room INTEGER NOT NULL, CONSTRAINT teacher_pk PRIMARY KEY (person_id), CONSTRAINT fk_teacher_person FOREIGN KEY (person_id) REFERENCES Person (person_id) ON DELETE RESTRICT ON UPDATE CASCADE);
CREATE TABLE Course (course_code TEXT NOT NULL, name TEXT NOT NULL, duration INTEGER NOT NULL, price INTEGER NOT NULL, description TEXT NOT NULL, PRIMARY KEY (course_code));
CREATE UNIQUE INDEX kurs_ben ON Course (name);
CREATE TABLE CourseInstance (course TEXT NOT NULL, start_date DATE NOT NULL, teacher TEXT NOT NULL, room INTEGER NOT NULL, CONSTRAINT courseinstance_pk PRIMARY KEY (course, start_date), CONSTRAINT fk_courseinstance_course FOREIGN KEY (course) REFERENCES Course (course_code) ON DELETE RESTRICT ON UPDATE CASCADE, CONSTRAINT fk_courseinstance_teacher FOREIGN KEY (teacher) REFERENCES Teacher (person_id) ON DELETE RESTRICT ON UPDATE CASCADE, CONSTRAINT fk_courseinstance_room FOREIGN KEY (room) REFERENCES Room (id) ON DELETE RESTRICT ON UPDATE CASCADE);
CREATE TABLE Participation (student TEXT NOT NULL, course TEXT NOT NULL, start_date DATE NOT NULL, CONSTRAINT participation_pk PRIMARY KEY (course, start_date, student), CONSTRAINT fk_participation_courseinstance FOREIGN KEY (course, start_date) REFERENCES CourseInstance (course, start_date) ON DELETE RESTRICT ON UPDATE CASCADE, CONSTRAINT fk_participation_student FOREIGN KEY (student) REFERENCES Student (person_id) ON DELETE RESTRICT ON UPDATE CASCADE);
