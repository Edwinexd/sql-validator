CREATE TABLE Person (personnummer TEXT NOT NULL, namn TEXT NOT NULL, adress TEXT NOT NULL, postnr TEXT NOT NULL, ort TEXT NOT NULL, telefon TEXT NOT NULL, PRIMARY KEY (personnummer));
CREATE TABLE Rum (id INTEGER NOT NULL, namn TEXT NOT NULL, antalplatser INTEGER NOT NULL, PRIMARY KEY (id));
CREATE UNIQUE INDEX rum_namn ON Rum (namn);
CREATE TABLE Student (personnummer TEXT NOT NULL, funktionshindrad BOOLEAN NOT NULL, CONSTRAINT STUDENT_PK PRIMARY KEY (personnummer), CONSTRAINT Student_Person_FK FOREIGN KEY (personnummer) REFERENCES Person (personnummer) ON DELETE RESTRICT ON UPDATE CASCADE);
CREATE TABLE Lärare (personnummer TEXT NOT NULL, tjänsterum INTEGER NOT NULL, CONSTRAINT teacher_pk PRIMARY KEY (personnummer), CONSTRAINT fk_teacher_person FOREIGN KEY (personnummer) REFERENCES Person (personnummer) ON DELETE RESTRICT ON UPDATE CASCADE);
CREATE TABLE Kurs (kurskod TEXT NOT NULL, namn TEXT NOT NULL, längd INTEGER NOT NULL, pris INTEGER NOT NULL, beskrivning TEXT NOT NULL, PRIMARY KEY (kurskod));
CREATE UNIQUE INDEX kurs_ben ON Kurs (namn);
CREATE TABLE Kurstillfälle (kurs TEXT NOT NULL, startdatum DATE NOT NULL, lärare TEXT NOT NULL, rum INTEGER NOT NULL, CONSTRAINT courseinstance_pk PRIMARY KEY (kurs, startdatum), CONSTRAINT fk_courseinstance_course FOREIGN KEY (kurs) REFERENCES Kurs (kurskod) ON DELETE RESTRICT ON UPDATE CASCADE, CONSTRAINT fk_courseinstance_teacher FOREIGN KEY (lärare) REFERENCES Lärare (personnummer) ON DELETE RESTRICT ON UPDATE CASCADE, CONSTRAINT fk_courseinstance_room FOREIGN KEY (rum) REFERENCES Rum (id) ON DELETE RESTRICT ON UPDATE CASCADE);
CREATE TABLE Deltagande (student TEXT NOT NULL, kurs TEXT NOT NULL, startdatum DATE NOT NULL, CONSTRAINT participation_pk PRIMARY KEY (kurs, startdatum, student), CONSTRAINT fk_participation_courseinstance FOREIGN KEY (kurs, startdatum) REFERENCES Kurstillfälle (kurs, startdatum) ON DELETE RESTRICT ON UPDATE CASCADE, CONSTRAINT fk_participation_student FOREIGN KEY (student) REFERENCES Student (personnummer) ON DELETE RESTRICT ON UPDATE CASCADE);
